Discords = {
	["Connect"] = "",
	["Disconnect"] = "",
	["Airport"] = "",
	["Deaths"] = "",
	["Policia"] = "",
	["Prison"] = "",
	["Paramedico"] = "",
	["Hackers"] = "",
	["Gemstone"] = "",
